# Program Menulis_1_Sampai_N
# Menuliskan 1 sampai N dalam satu baris

# KAMUS
# N, i: int

# ALGORITMA
# Masukkan nilai N
N = int(input('Masukkan N: '))
# Proses looping
for i in range(N):
    print(i+1, end=' ') # Output